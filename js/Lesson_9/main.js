let f2 = document.forms.form2;
f2.onsubmit = function (e) {
    e.preventDefault();
    let row = this.row;
    let column = this.column;
    let content = this.content;
    let tableBody = document.getElementById('table');

    for (let i = 0; i < row.value; i++) {

        tableBody.innerHTML  += `<tr class="row${i}"></tr>`

        for (let j = 0; j < column.value; j++) {
            document.getElementsByClassName(`row${i}`)[0].innerHTML += `<td class="content">${content.value}</td>`
        }
    }

};